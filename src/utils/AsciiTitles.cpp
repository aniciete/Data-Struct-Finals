    /**
     * @brief Retrieves the ASCII art for a given menu title.
     * 
     * This function looks up the provided title in the asciiMap.
     * If a matching title is found, a constant reference to the ASCII art is returned.
     * To avoid creating new strings for failed lookups, a static empty string is
     * returned when the title is not found.
     * 
     * @param title The title of the menu to retrieve the ASCII art for.
     * @return A constant reference to the ASCII art string, or an empty string if not found.
     */
    const std::string& getAsciiArt(const std::string& title) {
        // A static empty string to return for failed lookups, avoiding new allocations.
        static const std::string empty = "";
        // Find the title in the map.
        auto it = asciiMap.find(title);
        // Return the art if found, otherwise return the empty string.
        return (it != asciiMap.end()) ? it->second : empty;
    }
